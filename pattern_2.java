public class pattern_2 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("\n");
            int j = 1;
            while (j <= i) {
                System.out.print( " * ");
                j++;
            }
            i++;

        }

    }
}


