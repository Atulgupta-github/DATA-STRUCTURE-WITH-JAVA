public class pattern_3 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("\n");
            int j = 1;
            while (j <= 5) {
                System.out.print(" "+i+" ");
                j++;
            }
            i++;

        }

    }
}



